CREATE TABLE ACCESS_CONTROL_ENTRIES(id VARCHAR(64) PRIMARY KEY, user VARCHAR(32) NOT NULL, device_id VARCHAR(32) NOT NULL, role VARCHAR(10) NOT NULL, removed BOOLEAN DEFAULT FALSE, update_time TIMESTAMP NOT NULL);
CREATE INDEX INDEX_ACCESS_CONTROL_ENTRIES_USER ON ACCESS_CONTROL_ENTRIES (user);
CREATE INDEX INDEX_ACCESS_CONTROL_ENTRIES_DEVICE_ID ON ACCESS_CONTROL_ENTRIES (device_id);
