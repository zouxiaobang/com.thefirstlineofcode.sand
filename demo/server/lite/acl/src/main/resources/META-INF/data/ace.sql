CREATE TABLE ACCESS_LIST_ENTRIES(id VARCHAR(64) PRIMARY KEY, device_id VARCHAR(32) NOT NULL, user VARCHAR(32) NOT NULL, role VARCHAR(10) NOT NULL, removed BOOLEAN DEFAULT FALSE, update_time TIMESTAMP NOT NULL);
CREATE INDEX INDEX_ACCESS_LIST_ENTRIES_USER ON ACCESS_LIST_ENTRIES (user);
CREATE INDEX INDEX_ACCESS_LIST_ENTRIES_DEVICE_ID ON ACCESS_LIST_ENTRIES (device_id);
